package com.example.pizza;

public class Pizza {
    //Constructor, getters and setters for pizza variables
    double sizePrice;
    double crustPrice;

    double cheese_Price = 0;
    double pepperoniPrice = 0;
    double chickenPrice = 0;
    double beefPrice = 0;
    double baconPrice = 0;
    double olivesPrice = 0;
    double pineapplePrice = 0;
    double mushroomPrice = 0;
    double capsicumPrice = 0;

    public Pizza() {
    }

    public double getSizePrice() {
        return sizePrice;
    }

    public void setSizePrice(double sizePrice) {
        this.sizePrice = sizePrice;
    }

    public double getCrustPrice() {
        return crustPrice;
    }

    public void setCrustPrice(double crustPrice) {
        this.crustPrice = crustPrice;
    }

    public double getCheese_Price() {
        return cheese_Price;
    }

    public void setCheese_Price(double cheese_Price) {
        this.cheese_Price = cheese_Price;
    }

    public double getPepperoniPrice() {
        return pepperoniPrice;
    }

    public void setPepperoniPrice(double pepperoniPrice) {
        this.pepperoniPrice = pepperoniPrice;
    }

    public double getChickenPrice() {
        return chickenPrice;
    }

    public void setChickenPrice(double chickenPrice) {
        this.chickenPrice = chickenPrice;
    }

    public double getBeefPrice() {
        return beefPrice;
    }

    public void setBeefPrice(double beefPrice) {
        this.beefPrice = beefPrice;
    }

    public double getBaconPrice() {
        return baconPrice;
    }

    public void setBaconPrice(double baconPrice) {
        this.baconPrice = baconPrice;
    }

    public double getOlivesPrice() {
        return olivesPrice;
    }

    public void setOlivesPrice(double olivesPrice) {
        this.olivesPrice = olivesPrice;
    }

    public double getPineapplePrice() {
        return pineapplePrice;
    }

    public void setPineapplePrice(double pineapplePrice) {
        this.pineapplePrice = pineapplePrice;
    }

    public double getMushroomPrice() {
        return mushroomPrice;
    }

    public void setMushroomPrice(double mushroomPrice) {
        this.mushroomPrice = mushroomPrice;
    }

    public double getCapsicumPrice() {
        return capsicumPrice;
    }

    public void setCapsicumPrice(double capsicumPrice) {
        this.capsicumPrice = capsicumPrice;
    }
}
